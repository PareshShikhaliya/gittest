package fifth;

public class Example6 {

    public static void main(String[] args) {
        int i = 1;
        System.out.println("increment loop");
        while (i <= 10) {
            System.out.println(i);
            i++;
        }
        System.out.println("decrement loop");
        while (i > 0) {
            System.out.println(i);
            i--;
        }
    }

}

